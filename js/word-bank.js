/**
 * 隨機取一組題目，50% 機率互換平民/間諜詞
 */
export function getRandomWordPair() {
  const idx = Math.floor(Math.random() * WORD_BANK.length);
  const [w1, w2, category] = WORD_BANK[idx];
  const swap = Math.random() < 0.5;
  return {
    civilian: swap ? w2 : w1,
    spy: swap ? w1 : w2,
    category,
  };
}
